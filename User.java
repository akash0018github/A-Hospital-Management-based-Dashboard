    public void AutoID() {
        try {
            Statement s = con.createStatement();
            rs = s.executeQuery("SELECT MAX(ID) as max_id FROM user");
            rs.next();
            int maxId = rs.getInt("max_id");

            // If no ID is present
            if (maxId == 0) {
                lblpno.setText("10001");
            } else {
                maxId++; // Increment the max ID
                lblpno.setText(String.valueOf(maxId)); // Display the new ID
            }
        } catch (SQLException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    


    
    
