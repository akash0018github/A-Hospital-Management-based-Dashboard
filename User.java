    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    String name = txtname.getText();
    String age = txtage.getText();
    String gender = (String) txtgender.getSelectedItem();
    String address = txtaddress.getText();
    String phone = txtphone.getText();
    String consultation = (String) txtutype.getSelectedItem();

    // Check for empty fields
    if (name.isEmpty() || age.isEmpty() || gender == null || address.isEmpty() || phone.isEmpty() || consultation == null) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return; // Exit if any field is empty
    }

    System.out.println("Inserting User: " + name + ", " + age + ", " + gender + ", " + address + ", " + phone + ", " + consultation);

    try {
        // Prepare SQL insert statement
        pst = con.prepareStatement("INSERT INTO user(name, age, gender, address, phone, consultation) VALUES(?,?,?,?,?,?)");
        pst.setString(1, name);
        pst.setString(2, age);
        pst.setString(3, gender);
        pst.setString(4, address);
        pst.setString(5, phone);
        pst.setString(6, consultation);

        // Execute the update
        int affectedRows = pst.executeUpdate();

        // Check if the insertion was successful
        if (affectedRows > 0) {
            // Get generated ID and format it
            AutoID();  // Generate new ID for next entry

            // Update the table model with the new user data
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            Object[] newRow = { lblpno.getText(), name, age, gender, address, phone, consultation };
            df.addRow(newRow);

            JOptionPane.showMessageDialog(this, "User added successfully!");

            // Clear input fields
            txtname.setText("");
            txtage.setText("");
            txtgender.setSelectedItem(null);
            txtaddress.setText("");
            txtphone.setText("");
            txtutype.setSelectedItem(null);
            txtname.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "Error: Unable to insert user.");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

